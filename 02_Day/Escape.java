public class Escape {
    public static void main(String[] args) {
        System.out.println("Hello, \"Good Morning\"");
        System.out.println("Hello, \'Good Morning\'");
        System.out.println("Hello, \t\t\tGood Morning");
        System.out.println("Hello, \nGood Morning");
        System.out.println("Hello, \bGood Morning");
    }
}
